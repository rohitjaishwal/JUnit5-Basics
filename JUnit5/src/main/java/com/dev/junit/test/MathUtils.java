package com.dev.junit.test;

public class MathUtils {
	
	public int add(int a, int b) {
		return a+b;
	}
	
	public int sub(int a,int b) {
		return a-b;
	}
	
	public int mul(int a, int b) {
		return a*b;
	}
	
	public double div(double a, double b) {
		return a/b;
	}
	
	public int add(int a, int b, int c) {
		return a+b+c;
	}
}
